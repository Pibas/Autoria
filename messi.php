    <p>Lionel Andrés Messi, conhecido como Messi, nasceu em 24 de junho de 1987 em Rosario, Argentina. Desde pequeno mostrou talento no futebol e, após se mudar para a Espanha, se tornou um dos maiores ícones do Barcelona.</p>
