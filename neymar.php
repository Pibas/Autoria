    <p>Neymar da Silva Santos Júnior, mais conhecido como Neymar Jr., nasceu em 5 de fevereiro de 1992 em Mogi das Cruzes, São Paulo. É um dos jogadores mais talentosos e reconhecidos do futebol mundial.</p>
